					<h4 class="text-center" itemprop="name">What is fiber optic internet?</h4>
					<div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
						<div itemprop="text">
							<p>As the name would suggest, fiber optic internet is internet that is provided through special fibers or strands that are bundled into optic cables. Unlike copper lines, which are typically used for cable or DSL services, fiber optic lines are composed of plastic or glass strands that are used to transmit light. Light moves and reflects along the bends and curves in the optical cable. The light transmits information through encoded light signals, or binary. This is then decoded to present information (images, text, sounds, etc.) to the end user. This particular transmission method can transport more data than copper, which was originally designed to transmit only voice (i.e., early telephone services). When it comes to today’s digital requirements, fiber optic internet is significantly quicker and more reliable than its predecessors. A fiber optic internet connection allows users to access, upload and transmit information at speeds that surpass traditional internet mediums, such as copper based DSL or cable connections.</p><p>Using fiber optic cables, which bundle glass strands to transmit data, a fiber optic internet connection offers high speed upload and download times. Fiber internet speed does not degrade over longer distances, like copper based internet connections. Additionally, a fiber connection allows for greater data capacity while limiting loss and interference. As it becomes more available and to satisfy user demands, fiber optic internet is becoming the main source of competition for broadband, DSL, cable, and satellite internet services</p>
