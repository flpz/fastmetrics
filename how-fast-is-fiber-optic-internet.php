								<li><strong>1975</strong> – the U.S. government had used fiber optic cables to link two computers, reducing interference.</li>
								<li><strong>1977</strong> – Chicago became home to the first fiber optic telephone communication systems, which were buried underneath the city. At that time, the optical fiber carried what is equivalent to 672 voice channels.</li>
								<li><strong>1988</strong> – the first transatlantic fiber-optic cable, TAT-8, successfully transmitted data across the ocean floor from New Jersey to England and France. TAT-8 was less than 1 inch thick in diameter. It was capable of handling 40,000 simultaneous voice calls. At the close of the century, 80% of long-distance telephone traffic was traversing the globe via fiber optic cables.</li>
