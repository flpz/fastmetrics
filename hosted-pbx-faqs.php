								<p>A hosted Private Branch Exchange (PBX), also commonly known as a cloud-based PBX, is a private telephone service that enables organizations to make internal and external calls and connections via the Internet.</p>
								<p>A hosted PBX offers access to a variety of different <a href=""https://www.fastmetrics.com/cloud-phone-service-features.php">calling and communication features</a>. Some of these features may include;</p>
								<ul>
