								<p>PBX is the abbreviation of <strong>P</strong>rivate <strong>B</strong>ranch <strong>E</strong>xchange. A PBX is a phone system that is used within a business or organization to make internal calls (employee to employee) as well as external calls (employee to outside callers).</p>
