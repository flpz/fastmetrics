									<li>Call groups and conference calling</li>
									<li>Call intercept</li>
									<li>CRM integration</li>
									<li>Hunt groups</li>
									<li>Softphones</li>
									<li>Mobile app integrations</li>
									<li>Presence</li>
									<li>….and many more</li>
								</ul>
								<p>The aim of modern features associated with a cloud or hosted PBX, is to create a streamlined and efficient phone service. This applies to users both internal and external users of the phone system.</p>
								<p>A hosted PBX is managed and maintained by a voice or Internet Service Provider. This means there is no requirement for costly equipment installation. Maintenance and management of this type of phone system is all taken care of by the offsite service provider who hosts the PBX.</p>
