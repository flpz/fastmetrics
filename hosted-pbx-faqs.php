						</div>
						<div id="work" class="collapse" data-parent="#accordion">
						    <div class="card-body" itemprop="acceptedAnswer" itemscope itemtype="http://schema.org/Answer">
								<span itemprop="text">
									<p>A traditional PBX setup consists of on premise hardware, such as cabinets. A PBX enables individuals within an organization to make internal and external phone calls.</p>
									<p>To make external calls, a PBX provides phone lines that connect employees with the Public Switch Telephony Network (PSTN).</p>
									<p>Today, many organizations opt for cloud based or <strong>hosted PBX</strong> services. These require an <a href="https://www.fastmetrics.com/fiber-optic-internet.php">Internet connection with adequate bandwidth</a> or <a href="https://www.fastmetrics.com/multiprotocol-label-switching-mpls.php">MPLS to prioritize voice traffic</a>, when required.</p>
									<p>The basic function of a hosted PBX is the same as a traditional on-premise PBX. A major difference is an external phone or voice service provider will “host” and manage the PBX for end users. Hosted PBXs or cloud PBX systems are commonly hosted in data centers.</p>
									<p>Instead of routing calls through on-premise hardware, voice data is sent using the Internet to the hosted PBX.</p>
