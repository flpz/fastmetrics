								<p>When viewed over time, fiber optic internet is considered to be a relatively new technology.</p>
								<p><strong>The foundational innovations behind fiber optic internet actually date as far back as 2500 B.C.</strong> At that time, the Romans found it was possible to draw glass into fibers.</p>
								<p><strong>Between the 1700s and the 1800s, the roots of fiber optic communication experienced significant growth,</strong> with the invention of the optical telegraph in 1790 (Claude Chappe) and the Photophone in 1880 (Alexander Graham Bell).</p>
								<p><strong>Also during the mid-1800s, two valuable observations made by Daniel Colladon (1841) and John Tyndall (1854),</strong> both of which discovered the ability to conduct light through streams of water, further set the stage for fiber optic theory.</p>
								<p><strong>By 1926, RCA Rocky point Laboratory in Long Island, NY, worked with Clarence Hansel</strong> to outline the principles (and later patents in the U.S.A and Britain) of, fiber-optic image bundling.</p>
								<p>Following that, the world experienced a surge of advancements in the design and use of communication fibers. Each advancement paved the way for the fiber optic service we know today.</p>
								<p><strong>In the early 1970s, Corning Glass researchers invented Optical Waveguide Fibers, commonly known as fiber optic wire.</strong> This innovation allowed for the transfer of information via light wave patterns. This information could then be decoded at the final destination. The information transfer rate was 65,000 times faster than copper wire, showing researchers the true potential of fiber optics.</p>
