						    <h3><a class="card-link" data-toggle="collapse" href="#what"><i class="fa fa-angle-down"></i><span itemprop="name" class="speakable">What is fiber optic internet?</span></a></h3>
						</div>
						<div id="what" class="collapse" data-parent="#accordion">
						    <div class="card-body" itemprop="acceptedAnswer" itemscope itemtype="http://schema.org/Answer">
								<span itemprop="text">
								<p>As the name would suggest, fiber optic internet is internet that is provided through special fibers or strands that are bundled into optic cables.</p>
								<p>Unlike copper lines, which are typically used for cable or DSL services, fiber optic lines are composed of plastic or glass strands that are used to transmit light. Light moves and <a href="https://www.fastmetrics.com/blog/tech/fiber-optic-cables-how-they-work/">reflects along the bends and curves in the optical cable</a>.</p>
								<p>The light transmits information through encoded light signals, or binary. This is then decoded to present information (images, text, sounds, etc.) to the end user.</p>
								<p>This particular transmission method can transport more data than copper, which was originally designed to transmit only voice (i.e., early telephone services). When it comes to today’s digital requirements, fiber optic internet is significantly quicker and more reliable than its predecessors.</p>
								<p>A fiber optic internet connection allows users to access, upload and transmit information at speeds that surpass traditional internet mediums, such as copper based DSL or cable connections.</p>
								<p>Using fiber optic cables, which bundle glass strands to transmit data, a fiber optic internet connection offers high speed upload and download times. Fiber internet speed does not degrade over longer distances, like copper based internet connections. Additionally, a fiber connection allows for greater data capacity while limiting loss and interference.</p>
								<p>As it becomes more available and to satisfy user demands, fiber optic internet is becoming the main source of competition for broadband, DSL, cable, and satellite internet services</p>
